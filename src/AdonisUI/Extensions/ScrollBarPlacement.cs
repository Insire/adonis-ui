namespace AdonisUI.Extensions
{
    /// <summary>
    /// Values for controlling the placement of scroll bars.
    /// </summary>
    public enum ScrollBarPlacement
    {
        /// <summary>
        /// Place the scroll bar next to content (default).
        /// </summary>
        Docked,

        /// <summary>
        /// Place the scroll bar on top of content.
        /// </summary>
        Overlay,
    }
}
